using System;
using System.Collections.Generic;
using System.Linq;
using EDMs.Data.DAO.Security;
using EDMs.Data.Entities;

namespace EDMs.Business.Services.Security
{
    /// <summary>
    /// The category service.
    /// </summary>
    public class WaitingSyncDataService
    {
        /// <summary>
        /// The repo.
        /// </summary>
        private readonly WaitingSyncDataDAO repo;

        /// <summary>
        /// Initializes a new instance of the <see cref="WaitingSyncDataService"/> class.
        /// </summary>
        public WaitingSyncDataService()
        {
            this.repo = new WaitingSyncDataDAO();
        }

        #region Get (Advances)
        #endregion

        #region GET (Basic)
        /// <summary>
        /// Get All Categories
        /// </summary>
        /// <returns>
        /// The category
        /// </returns>
        public List<WaitingSyncData> GetAll()
        {
            return this.repo.GetAll().ToList();
        }

        public List<WaitingSyncData> GetAllActive()
        {
            return this.repo.GetAll().Where(t => !t.IsSynced.GetValueOrDefault()).ToList();
        }

        /// <summary>
        /// Get Resource By ID
        /// </summary>
        /// <param name="id">
        /// ID of category
        /// </param>
        /// <returns>
        /// The category</returns>
        public WaitingSyncData GetById(int id)
        {
            return this.repo.GetById(id);
        }
        #endregion

        #region Insert, Update, Delete
        /// <summary>
        /// Insert Resource
        /// </summary>
        /// <param name="bo"></param>
        /// <returns></returns>
        public int? Insert(WaitingSyncData bo)
        {
            return this.repo.Insert(bo);
        }

        public bool Update(WaitingSyncData bo)
        {
            try
            {
                return this.repo.Update(bo);
            }
            catch (Exception)
            {
                return false;
            }
        }

        /// <summary>
        /// Delete Resource
        /// </summary>
        /// <param name="bo"></param>
        /// <returns></returns>
        public bool Delete(WaitingSyncData bo)
        {
            try
            {
                return this.repo.Delete(bo);
            }
            catch (Exception)
            {
                return false;
            }
        }

        /// <summary>
        /// Delete Resource By ID
        /// </summary>
        /// <param name="id"></param>
        /// <returns></returns>
        public bool Delete(int id)
        {
            try
            {
                return this.repo.Delete(id);
            }
            catch (Exception)
            {
                return false;
            }
        }
        #endregion
    }
}
